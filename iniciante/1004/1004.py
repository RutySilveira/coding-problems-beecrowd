# Lê um número inteiro do usuário e armazena na variável 'a'
a = int(input())

# Lê outro número inteiro do usuário e armazena na variável 'b'
b = int(input())

# Calcula o produto dos valores armazenados em 'a' e 'b'
prod = a * b

# Imprime o resultado do produto no formato "PROD = valor"
print("PROD =", prod)
